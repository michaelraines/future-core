// DebugPrint draws msg on the image at the default position (1, 1).
// It uses a built-in monospace bitmap font, matching Ebitengine's
// ebitenutil.DebugPrint.
func DebugPrint(img *futurerender.Image, msg string) {
	DebugPrintAt(img, msg, 1, 1)
}

// DebugPrintAt draws msg on the image at position (ox, oy).
// It uses a built-in monospace bitmap font, matching Ebitengine's
// ebitenutil.DebugPrintAt. Newlines produce multiple lines.
func DebugPrintAt(img *futurerender.Image, msg string, ox, oy int) {
	if img == nil || msg == "" {
		return
	}

	atlas := debugFontAtlas()
	lines := strings.Split(msg, "\n")
	for li, line := range lines {
		for ci, ch := range line {
			idx := int(ch) - debugCharStart
			if idx < 0 || idx >= debugCharCount {
				continue
			}
			sx := (idx % debugCols) * debugCharW
			sy := (idx / debugCols) * debugCharH
			sub := atlas.SubImage(image.Rect(sx, sy, sx+debugCharW, sy+debugCharH))

			opts := &futurerender.DrawImageOptions{}
			opts.GeoM.Translate(float64(ox+ci*debugCharW), float64(oy+li*debugCharH))
			img.DrawImage(sub, opts)
		}
	}
}

const (
	debugCharW     = 6  // glyph cell width in pixels
	debugCharH     = 16 // glyph cell height in pixels (includes line gap)
	debugCharStart = 32 // first ASCII character (space)
	debugCharEnd   = 127
	debugCharCount = debugCharEnd - debugCharStart // 95 printable chars
	debugCols      = 16                            // glyphs per atlas row
)

var (
	debugAtlasOnce sync.Once
	debugAtlasImg  *futurerender.Image
)

// debugFontAtlas returns a lazily-initialized atlas image containing all
// printable ASCII characters rendered in white on a transparent background.
func debugFontAtlas() *futurerender.Image {
	debugAtlasOnce.Do(func() {
		rows := (debugCharCount + debugCols - 1) / debugCols
		atlasW := debugCols * debugCharW
		atlasH := rows * debugCharH

		rgba := image.NewRGBA(image.Rect(0, 0, atlasW, atlasH))
		white := color.RGBA{R: 255, G: 255, B: 255, A: 255}

		for i := 0; i < debugCharCount; i++ {
			ch := rune(debugCharStart + i)
			glyph := debugGlyph(ch)
			if glyph == nil {
				continue
			}
			cx := (i % debugCols) * debugCharW
			cy := (i / debugCols) * debugCharH
			// Vertical offset to center the glyph in the cell.
			yOff := cy + (debugCharH-len(glyph))/2
			for row, bits := range glyph {
				for col := 0; col < debugCharW-1; col++ {
					if bits&(1<<(4-col)) != 0 {
						rgba.Set(cx+col, yOff+row, white)
					}
				}
			}
		}

		debugAtlasImg = futurerender.NewImageFromImage(rgba)
	})
	return debugAtlasImg
}

// debugGlyph returns the bitmap data for a character as a slice of bytes,
// where each byte represents a row and bits 4..0 represent pixels left-to-right.
// Returns nil for unsupported characters.
func debugGlyph(ch rune) []byte {
	idx := int(ch) - debugCharStart
	if idx < 0 || idx >= len(debugFontData) {
		return nil
	}
	return debugFontData[idx]
}

